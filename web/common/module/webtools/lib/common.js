/**
 * 获取MD5加密字符串
 * @param str
 * @returns {*|Hmac.digest}
 * @constructor
 */
exports.GetMD5 = function(str) {
    var crypto = require('crypto');
    var md5 = crypto.createHash('md5');
    return md5.update(str).digest('base64');
}

exports.CheckVer = function(oldVer, newVer) {
    var OldVerSub = oldVer.split('.');
    var NewVerSub = newVer.split('.');
    if (Number(OldVerSub[0]) < Number(NewVerSub[0])) {
        return true;
    } else {
        if (Number(OldVerSub[1]) < Number(NewVerSub[1])) {
            return true;
        } else {
            if (Number(OldVerSub[2]) < Number(NewVerSub[2])) {
                return true;
            }
        }
    }
    return false;
}

/**
 * 返回GUID
 *  uuid(8, 16) // "098F4D35"
 *  uuid(8, 10) // "47473046"
 *  uuid(8, 2)  //  "01001010"
 * @param {Object} len
 * @param {Object} radix
 */
exports.UUID = function(len, radix) {
    var chars = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'.split('');
    var uuid = [],
        i;
    radix = radix || chars.length;

    if (len) {
        // Compact form
        for (i = 0; i < len; i++)
            uuid[i] = chars[0 | Math.random() * radix];
    } else {
        // rfc4122, version 4 form
        var r;

        // rfc4122 requires these characters
        uuid[8] = uuid[13] = uuid[18] = uuid[23] = '-';
        uuid[14] = '4';

        // Fill in random data.  At i==19 set the high bits of clock sequence as
        // per rfc4122, sec. 4.1.5
        for (i = 0; i < 36; i++) {
            if (!uuid[i]) {
                r = 0 | Math.random() * 16;
                uuid[i] = chars[(i == 19) ? (r & 0x3) | 0x8 : r];
            }
        }
    }

    return uuid.join('');
}

