exports.FixLen = function(string, len) {
    var str = string;
    if (str.length > len) {
        str = string.substring(0, len) + "...";
    }
    return str;
}

/**
 * 保留num位小数，不足补0
 */
exports.FormatNumber = function(value, num) {
    var a, b, c, i;
    a = value.toString();
    b = a.indexOf(".");
    c = a.length;
    if (num == 0) {
        if (b != -1) {
            a = a.substring(0, b);
        }
    } else { //如果没有小数点
        if (b == -1) {
            a = a + ".";
            for (i = 1; i <= num; i++) {
                a = a + "0";
            }
        } else { //有小数点，超出位数自动截取，否则补0
            a = a.substring(0, b + num + 1);
            for (i = c; i <= b + num; i++) {
                a = a + "0";
            }
        }
    }
    return a;
}

